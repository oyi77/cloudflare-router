function getLanguage(langCode = 'en') {
  return languages[langCode] || languages.en;
}

function translate(key, langCode = 'en') {
  const lang = getLanguage(langCode);
  return lang.translations[key] || key;
}

function getAvailableLanguages() {
  return Object.entries(languages).map(([code, lang]) => ({
    code,
    name: lang.name,
    flag: lang.flag
  }));
}

function getUserLanguage(req) {
  const acceptLang = req.headers['accept-language'] || 'en';
  const langCode = acceptLang.split(',')[0].split('-')[0].toLowerCase();
  return languages[langCode] ? langCode : 'en';
}

function i18nMiddleware(req, res, next) {
  req.lang = req.query.lang || getUserLanguage(req);
  req.t = (key) => translate(key, req.lang);
  next();
}

module.exports = {
  languages,
  getLanguage,
  translate,
  getAvailableLanguages,
  getUserLanguage,
  i18nMiddleware
};
